<h2>UH31 古芝地道游</h2>
<p>
從港口出發大約3小時的車程後我們將到達[古芝地道]。該地道原為越南抵抗法國統治時，由當時農兵徒手挖了20多年的一個地下戰道。到了1960年代越戰時期轉為實施抗美地道戰的重要地下基地，由越南南方民族解放陣線（越共）建造，全長250公里，由無數條寬不到80公分的地道分為三層結構交錯而成。地道中建築有廚房、醫院、睡眠房間、會議室和指揮戰斗室、武器改裝室等設備，規劃極為完備。現今古芝地道儼然成為了胡志明市的著名參觀景點，是國家級的歷史建築文物。然後前往臨近的[BENNAY花園餐廳]，這是以紅頂白牆的傳統房屋樣式建造的餐廳，在綠草茵茵、湖水環繞的秀麗景色中享用午餐。
</br>
注：遊客需要在不平坦的路面上步行約1.5公里，大概40步石級。
</p>
<p>
需時:8小時30分
</p>
<p>
費用:$138美元/人
</p>

