<h1>芽莊•越南</h1>

<h2>NH17 芽莊精華遊 (NhaTrang City Highlights)</h2>
<p>
首先前往【隆山寺】，該寺廟佔地面積並不大，院子和寺廟裡卻常常擠滿前來清修拜佛的信徒。在寺廟的後山上，爬過192級台階，就會來到一尊24米高的白色佛像腳下，佛像靜坐在蓮花之上，俯瞰著芽莊。在這個露台上還可以看到芽莊的城市景觀。接下來我們將前往【婆那加佔婆塔】，該塔建於公元7-12世紀間。婆那加是音譯，也有翻譯作天依女神廟。這裡供奉的是天依女神（Po Nagar），天依女神是庇佑佔婆王國南部的一位神明，保佑著靠海吃飯的漁民。接下來我們會前往【情人石】，對岸的山沿像側臥的仙女，與腳下的掌印石一道描繪出美麗的傳說，這里風景美麗，是一個放鬆心情的好地方。隨後可前往【樂天瑪特】選購當地的特產。最後在登船之前參觀的【海洋研究院】，這是一座建立於1922年的法式建築，學院設有20個專門的部門和不同的展廳,並有多個陳列室展出多種海洋生物。行程預留了自理午餐的時間。
</p>
<p>
需時:5小時15分
</p>
<p>
費用:$49美元/人
</p>
