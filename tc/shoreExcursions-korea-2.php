<h1>釜山・韓國</h1>
<h2>KP26 釜山環城遊</h2>

<p>
首先登上【龍頭山】，俯瞰釜山的繁華街景和臨海風光，極具浪漫情調；高達120米的釜山塔更是當地的標誌性建築（不包含登塔安排），公園裡還有民族英雄李舜臣將軍的紀念銅像等。然後前往【釜山博物館】，自1978年開館以來，已經發展成釜山的文化地標，共有7個展廳和戶外展覽場，陳列著釜山慶尚地區發掘出來的佛像、石碑、建築等6,000多件珍貴的文化遺產，只有這裡能讓你了解到真正的釜山！行程途徑【聯合國紀念公園】，還包括釜山市區【樂天免稅店】，隨您自行購買心水潮流品牌。注：每逢週一【釜山博物館】暫停開放，建議選擇【釜山環城遊B】。
</p>
<p>
需時：5小時
</p>
<p>
費用：56美元/人
</p>
<p>&nbsp;</p>

<h2>KP59 釜山環城遊B</h2>
<p>
從港口出發前往【二妓台自然公園】，它的得名頗有歷史韻味，自然風光也很有特色。之前作為軍事作戰區嚴禁遊客進入，所以保存了很好的自然生態，為很多珍貴動植物的樂園，開放後旋即成為最受歡迎的自然公園。周邊是天然石岩屏障，粗獷的岩石與大海相依相連，勾畫出釜山最特別的海岸景色，還可遠眺釜山港著名的五六島。行程途徑【聯合國紀念公園】，還包括釜山市區【樂天免稅店】，隨您自行購買心水潮流品牌。【龍頭山】也是非到不可的景點，在這裡俯瞰釜山的繁華街景和臨海風光，極具浪漫情調，還有民族英雄李舜臣將軍的紀念銅像等。
</p>
<p>
需時：5小時
</p>
<p>
費用：56美元/人
</p>
<p>&nbsp;</p>

<h2>KP24 東海之約</h2>
<p>
從港口出發，前往韓國唯一一座海邊寺廟 —【海東龍宮寺】。從海邊到寺廟的108級梯階，代表著佛教經義的108種災厄，每走一級就祈求免去一種災厄，傳說海中還有觀音會乘龍出現。及後前往【海雲台】，這裡最有名的是長達2公里，蜿蜒曲折的白沙灘，一年四季漫步於波瀾起伏的海邊，是那麼的浪漫愜意！遊客亦可以前往市區的【樂天免稅店】自由購物。行程還包括被茂密的冬柏和松林圍繞，專為APEC 會議而建的【世峰樓】，陣陣松濤中背倚燈塔，遠眺大橋，釜山的傳統與現代盡在其中。行程已預留自費用餐的時間。
</p>
<p>
需時：7小時
</p>
<p>
費用：69美元/人
</p>
