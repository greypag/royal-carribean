<h2>HZ34 順化皇城遊</h2>
<p>
首先前往世界文化與自然遺產【順化皇城】，這是一座有著200 年歷史的宮殿建築群，曾經居住著阮氏王朝的末代皇帝。皇城為方形，有4 個城門，四周有護城河，香河蜿蜒流經都城、皇城、紫禁城和內城，給這座獨特的古城增添了自然美。然後前往臨近的【皇陵】，在綠水環繞的環境中，對這段歷史更添一份瞭解。隨後前往當地特色餐廳【古都順化】，在古色古香的傳統建築中享用當地美食。然後前往百年古剎【天姥寺】，這是一座有著400 年歷史的寺廟，寺前有福緣塔，塔前是與香江通連的天姥荷花潭景色綺麗。最後前往當地手工藝品商店【LUC BO】，欣賞手工藝品現場製作，選購特色商品，並稍作休息。行程已含餐。
</p>
<p>
時間：約 8 小時
</p>
<p>
價格：88 美金 / 人
</p>


