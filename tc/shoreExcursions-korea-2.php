<h2>KP25 巨濟島探幽</h2>
<p>
【巨濟島】是東北亞最精緻美麗的島嶼之一，純淨無污染的自然美景盡入眼簾。2010年底通車的【巨加大橋】，使我們得以輕鬆直達島上。巨加大橋總長度達8.2km，由海上斜張橋 (3.5km)、海底隧道 (3.7km)及路上隧道 (1.0km)組成。海上斜張橋與秀麗風光融為一體，猶如一條海上的寶石項鍊；而海底隧道最深處部分位於海底48m，更是當今世界的海底隧道之最。除了自然美景，巨濟島也頗具歷史韻味，朝鮮戰爭中修建起來的巨濟戰俘收容所是當時收容規模最大的地方，遺址目前已經發展成為一個公園。在巨濟島飽覽美景，抒發思古幽情之後將返回釜山享用美味餐食。回港前您還可到【樂天免稅店】自由選購潮流品牌。
</p>
<p>
需時：7小時
</p>
<p>
費用：89美元/人
</p>
