<h1>宮崎•日本</h1>
<h2>UB21 宫崎一日游</h2>
<p>
首先帶大家前往【宮崎青島】，這個與陸地一線相連的小島。受到波浪拍打侵蝕而形成了不可思議的波狀岩覆蓋，從高處鳥瞰就像是「魔鬼的洗衣板」，讓您不由自主地感歎大自然的鬼斧神工。島中央是具有1200多年歷史的青島神社，朱色大殿配上蔚藍的大海令人印象深刻，作為保佑美好的姻緣的神社而為人熟知。隨後前往【照葉大吊橋】，它處於綾町九州山地國家公園,也是日本百選自然風光之一。最後前往【亞熱帶作物園】，欣賞到平日不常看到的芒果、木瓜、金橘、向日夏柑橘等，徜徉在這南國的植物世界中。 行程包含用餐。 
</p>
<p>
需時:7小時 
</p>
<p>
費用:$79美元/人
</p>
