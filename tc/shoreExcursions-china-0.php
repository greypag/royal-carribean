<h1>三亞・中國</h1>
<h2>XY43 三亞風光半日遊 (Senic Sanya)</h2>
<p>
從碼頭乘車至【天涯海角】風景區。 </br>
【天涯海角】位於以美麗迷人的熱帶海濱自然風光、悠久獨特的歷史文化而馳名中外的三亞。這里海水澄碧，煙波浩瀚，帆影點點，椰林婆娑，奇石林立，水天一色。海灣沙灘上有大小百塊石聳立，“天涯石”、“海角石”、“日月石”和“南天一柱”突兀其間，昂首天外，崢嶸壯觀。在此享受休閒時光後乘車返回碼頭。
</p>
<p>
需時:3小時30分
</p>
<p>
費用:62美元/人
</p>
