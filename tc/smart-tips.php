  3. 日記– 紀錄每天的旅遊心情、期待日後的分享與回憶<br />
4. 通訊簿– 給好友們發送遊輪明信片，讓他們眼紅一下 </p>
<p>望遠鏡也是經常會被遺忘的旅行貼身寶物之一，出門前可別忘了帶上，這樣無論遠近，悅目美景都能一目了然。</p>
<p style="color:#195f9b; font-weight:bold; font-size:16px;">如何節省空間</p>
<p>別忘了在您的行李箱內預留空間，把親友的紀念品完好無損的帶回家。以下為你提供一些節省空間小提示： </p>
<p>- 攜帶可以搭配的褲子/裙子，把原來的3套衣裝舉一反三，變成5或6套讓人耳目一新的時尚服飾。 <br />
  - 在上下游輪時，穿上您認為最笨重的鞋子，這樣就不必再為打包煩惱。<br />
- 發揮以大裝小的原則，這樣就能騰出更多的空間。 <br />
- 多帶一些空塑料袋，以備不時之需。 </p>
<p>出席正式晚宴，女士必須穿著晚禮服，男士則必須以西裝、領帶或燕尾服打扮。至於半休閒的晚餐時間，女士則應身著洋裝衣裙，或是褲裝，男士則須披上夾克或外套。 </p>
<p>請記得，短褲、T裇及泳裝，都不是餐館或晚餐的合適穿著。在帆船咖啡廳，我們為您提供了半休閒晚餐時段，因此請務必以休閒服飾出席。另外，休閒服飾也適用於我們的所有特色風味餐館。 我們也提供租借燕尾服服務，供您出席正式晚宴及特別場合使用。</p>
<p style="color:#195f9b; font-weight:bold; font-size:16px;">岸上服飾 </p>
<p>您需要一雙舒適的步行鞋，以及一頂帽子和一支防曬霜，以抵抗室外強烈紫外線的侵害。 </p>
<p>若您選擇歐洲或阿拉斯加航程，入夜時分甲板上可能會較涼，我們建議您準備毛衣、夾克及長褲。至於遊覽任何博物館、教堂或宗教聖地時，含蓄保守的穿著絕對是明智之舉。或許，您也可以帶上雨衣、徒步鞋及折傘。</p>
<p style="color:#195f9b; font-weight:bold; font-size:16px;">不應攜帶物品</p>
