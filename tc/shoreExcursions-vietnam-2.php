<h1>胡志明市 ・越南</h1>
<h2>UH28 胡志明市精华游</h2>
<p>
此次行程我們將參觀胡志明市區的【歷史博物館】，館內展示著越南3300年以來各時期的珍貴文物，是了解越南歷史的最佳起點。隨後前往胡志明市的標誌景點【統一宮】，這裡原是法國殖民時期興建的“統督府”，在南北時期作為南越的政權中心改名為“獨立宮”，解放之後則稱為“統一宮”，作為越南人民爭取獨立統一的象徵。大廈內有多個裝飾華美、富麗堂皇的大小廳堂，可滿足外交、宴會、娛樂、居住、軍事指揮等各種需要，屋頂平台上還停放著總統當年乘坐過的同型號直升機，彷彿向後人講述這座老建築不同尋常的歷史。遊客還可以在【中央郵局】和【聖母大教堂】短暫停留拍照留念：建於十九世紀末的“中央郵局”是一座極富古典魅力的哥特式建築，至今仍在使用；而“聖母大教堂”因以紅色磚塊建造，又被稱為“紅教堂”。接下來前往【天后廟】，這是一座由越南華人於十九世紀建立的媽祖廟，位於第五郡的堤岸，廟內供奉著被中國東南沿海人奉為海神的媽祖。行程包含午餐和參觀當地【水漆畫工作坊】，可挑選一款精緻實用的水漆畫手工藝品作為紀念。最後遊覽【濱城市場】自由購物，帶著當地特產滿載回船。
</br>
注：車程大約2小時單程，30-40步石階。應穿舒適衣服和鞋子。</p>
<p>
需時:9小時
</p>
<p>
費用:$59美元/人
</p>
