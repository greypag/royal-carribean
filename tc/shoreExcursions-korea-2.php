<h2>KP60 慶州世界文化遺產之旅B</h2>
<p>
從釜山港出發，乘車前往“沒有圍牆的博物館”——慶州。首先遊覽世界文化遺產【佛國寺】，它創建於公元752 年，是統一新羅時代的代表性寺廟。建築華麗宏偉，呈現一種平衡和諧之美，被譽為韓國石造藝術的寶庫，最美佛寺的代表，香火不斷。之後來到【瞻星台】，這是東方現存最古老的天文台，建於新羅27代王善德女王時期，用以觀測天空中的氣象及星座，於1962年被指定為國寶第31號。夏令時分，一定要遊覽【雁鴨池】：據韓國最古老的史書《三國史記》記載，這是新羅文武王下令在宮城裡建造的蓮花池，是新羅時代宮殿中最雄壯，而且擁有最華麗荷花池的地方。行程包含當地特色午餐，回港前還有一些時間讓您到釜山市區的【樂天免稅店】選購潮流品牌。
