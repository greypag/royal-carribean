<h2>UH29 頭頓市環城遊</h2>
<p>
從港口出發，到達頭頓後我們會在98英尺（30米）高的耶穌巨像前稍作停留拍照留戀。隨後參觀位於山坡上的一座佛教寺。緊接著我們將到達有船灣之稱的半島，在那裡你會看到許多木製漁船停泊。鯨魚寺（音譯）是一個不容錯過的景點，關於這座寺廟還有一個美麗的傳說，最後我們到達BachDinh，參觀建於19世紀的皇家別墅，這裡原是修建於叢林圍繞的山腹中可觀海的皇家別墅，也是皇室的夏季行宮。
</br>
注：不含餐,雕像的可見性取決於植被（沿著山邊的樹木葉冠）。
</br>
車程大約45-60分鐘單程。山上的佛教寺需要步行2-3分鐘以及大約50步石階。
</p>
<p>
需時:4小時
</p>
<p>
費用:$29美元/人
</p>
