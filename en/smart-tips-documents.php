<p style="color:#195f9b; font-weight:bold; font-size:16px;">Caution</p>

<p> The requirements described below are required by government regulations and policies. They are subject to change without notice.
  </p>
<p>It is the sole responsibility of the guest to identify and obtain all required travel documents and have them available when necessary. These appropriate valid travel documents such as passports, visas, inoculation certificate and family legal documents are required for boarding and re-entry into the United States and other countries.
</p>
<p>Guests who do not possess the proper documentation may be prevented from boarding their flight or ship or from entering a country and may be subject to fines. No refunds will be given to individuals who fail to bring proper documentation. </p>
<p style="color:#195f9b; font-weight:bold; font-size:16px;">Consecutive Sailings</p>
<p>Guests on consecutive sailings must ensure they have the proper travel documents for their entire cruise vacation.</p>

<p style="color:#195f9b; font-weight:bold; font-size:16px;">Passport</p>
<p>- A valid passport is required for travel. <br />
  - Ensure sufficient blank visa pages within the passport for visa stamps. <br />
  - For your protection, we recommend that your passport expiration date not occur within six (6) months following the voyage termination date. <br />
  - Since the ship will collect your passport to speed the clearance formalities at each port of call, we suggest you bring with you a few photocopies of your passport's personal information page which includes your recent photo, and that you carry one of these photocopies with you each time you leave the ship.</p>
<p style="color:#195f9b; font-weight:bold; font-size:16px;">Visas </p>
<p>- It is the guests' responsibility to identify and to obtain all the necessary visas before leaving home, unless otherwise instructed below. <br />
  - Based on nationality, many countries still require an entry visa. <br />
  - Suggestions on where to get visa information: the Embassies (Consular Service Office) of each country on your sailing itinerary</p>
<p style="color:#195f9b; font-weight:bold; font-size:16px;">INOCULATION / HEALTH </p>
<p>As detailed in our brochure, all guests must ensure that they are medically and physically fit for travel. The Centers for Disease Control (CDC) and the World Health Organization (WHO) provide guidelines as to which vaccinations are required in each country. In many cases inoculations are recommended but in some circumstances they are required. We recommend that you check with your health care professional or a Travel Medicine Specialist certified by the WHO for guidance. Other informational resources can be accessed at the Centers for Disease Control and Prevention's Traveller's Health website at www.cdc.gov/travel or toll free at 1-877-FYI-TRIP, and the World Health Organisation website at <a href="http://www.who.int/vaccines-diseases/" target="new">www.who.int/vaccines-diseases/</a>.</p>
<p style="color:#195f9b; font-weight:bold; font-size:16px;">Family Legal Documents </p>
<p>Adults who are not the parent or Legal Guardian of any minor child travelling with them are required to present the child's valid passport and visa or the child's birth certificate (original, a notarised copy or a certified copy) and an original notarised letter signed by at least one of the child's parents. </p>
<p>The notarised letter from the child's parent must authorise the travelling adult to take the child on the specific cruise, must authorise guardian to sign legal documentation/waivers for participation in any activities requiring them (i.e. Rock Climbing, FlowriderÂ®, Bungee Trampoline, Inline Skating, or Ice Skating) and must authorise the travelling adult to supervise the child and permit any medical treatment that must be administered to the child. If a non- parent adult is a Legal Guardian, the adult must present a certified certificate of Guardianship with respect to the child.</p>

