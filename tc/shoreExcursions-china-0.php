<h2>XY44 三亞千古情半日遊 (The Legend of Romance in Sanya)</h2>
<p>
從碼頭乘車至【三亞千古情】景區。在這裡你可以見到星羅棋布的海南非物質文化遺產手工作坊、特色工藝品市集、海南特色小吃。更可以在南海女神廣場、崖州古城、科技遊樂館、三亞千古情大劇院、黎村和苗寨等六大主題遊玩區度過愉快的時光。在三亞千古情大劇院上演的大型歌舞《三亞千古情》濃縮了三亞萬年歷史文化與傳奇，其中包含有鹿回頭的美麗傳說，這裡能看到鑑真東渡時的驚濤駭浪，能聽到落筆洞的萬年迴聲，更有巾幗英雄冼夫人蕩氣迴腸的故事。遊覽結束後乘車返回碼頭。
</p>
<p>
需時:4小時
</p>
<p>
費用:77美元/人
</p>
