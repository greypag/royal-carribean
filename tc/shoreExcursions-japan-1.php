<h2>OK51 沖繩精華遊B (Okinawa)</h2>
<p>
首先遊覽【首裡城】，這座城堡是琉球國的都城所在地和王宮，是當地最大的木結構建築，融合了中國和日本傳統建築的特點，形態宏偉，色彩鮮豔。 您還可以比較主殿的雙層建築與北京故宮的太和殿的異同，緬懷琉球王朝的輝煌。 然後前往著名的【國際通】，又稱「奇跡的一裡」，街道兩邊百貨、餐廳、禮品特產應有盡有，您可以在休閒的同時自理用餐。【北谷美國村】也是必去的場所，這是一處利用美軍基地舊址整修而成的都市度假休閒區，豐富多彩的美食，悠閒輕鬆的氛圍，超值的紀念品商店，定能讓您流連忘返。
</p>
<p>
需時:5小時30分 
</p>
<p>
費用:$62美元/人
</p>

