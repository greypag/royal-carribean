<h2>KP02 慶州世界文化遺產之旅</h2>
<p>
從釜山港出發，乘車前往“沒有圍牆的博物館”——慶州。首先遊覽世界文化遺產【佛國寺】，它創建於公元752 年，是統一新羅時代的代表性寺廟。建築華麗宏偉，呈現一種平衡和諧之美，被譽為韓國石造藝術的寶庫，最美佛寺的代表，香火不斷。之後來到【大陵苑 ( 天馬塚)】，這是新羅王室貴族的陵園群。步行靠近，可以感受到現代和古代共存的奇妙，以“天馬塚” 和“南皇大塚”最為著名。天馬塚發掘於1970年，由於出土的馬鞍墊子上繪有天馬，而且是1,500多年前古新羅時代唯一留存的繪畫作品，因此十分引人注目。開放的王陵內部已經成為了博物館，王冠也被復原，展示著王的奢侈生活。在許多的歷史遺跡中信步走一圈，便完成了1,500多年的歷史旅行。行程包含當地特色午餐，回港前還有一些時間讓您到釜山市區的【樂天免稅店】選購潮流品牌。
</p>
<p>
需時：8小時
</p>
<p>
費用：99美元/人
</p>
<p>&nbsp;</p>

<h2>KP57 太宗台之旅</h2>
<p>
乘大巴前往【太宗台】，相傳新羅太宗武列王曾遊覽於此，因而得名。附近的松林、山茶樹生長茂盛，懸崖峭壁連綿不斷，遊客可以在此遠眺銀白如雪的燈塔，欣賞海岸風光。天朗氣清的話，甚至可以看到遠處的日本島嶼。之後會到市區的【樂天免稅店】，讓您自由選購潮流品牌。最後來到【札嘎其市場】，這裡是韓國最大的水產市場，也是釜山著名的十大風景名勝之一，您可以在此親身體驗當地居民的生活。
</p>
<p>
需時：5小時
</p>
<p>
費用：56美元/人
</p>
