<h2>HZ30 峴港經典遊 (Da Nang City Highlights)</h2>
<p>
首先前往【佔婆博物館】，該館坐落於城市中心的漢江岸邊，​​在法國遠東研究院支援下，建成於一九一五年。它同時是世界上展出佔婆藝術珍藏品最多的博物館。參觀博物館之後乘坐三​​輪車遊覽峴港市，欣賞越南的異域風情。接下來乘旅遊車前往【峴港五行山】，在山腳下石雕村里的其中一家展廳自由參觀各種石雕產品。然後沿著山水（美溪）海岸路前往山茶半島的【靈應寺】。它是一座高達68米的觀音佛像，面向大海有著得天獨厚的地理位置，在面海的半山上，站在哪個角度都能看見藍裡透著綠的海，保佑著沿海居民和出海漁民的平安。朝拜後在當地餐廳享用特色午餐。最後前往【樂天瑪特】，自由購物，完成愉快旅程。行程已含餐。
</p>
<p>
需時:8小時
</p>
