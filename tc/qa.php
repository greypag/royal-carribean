<h3>常見問題</h3>

<div style="position:relative; float:left; width:640px; left:30px;min-height:700px;">

<p style="color:#195f9b; font-weight:bold; font-size:16px;">
Q: 坐遊輪時會不會很容易暈船浪？
</p>
<p>
A: 皇家加勒比的遊輪噸位比較大，在海面上行駛會很穩定，設計上更添加了先進的平衡裝置，減少游輪的晃動。當旅客有需要時，亦可前往醫療中心或遊輪服務櫃檯索取暈船藥物。
</p>

<p style="color:#195f9b; font-weight:bold; font-size:16px;">
Q: 於船上遇上任何緊急或突發事故，該如何求助？
</p>

<p>
A: 在船上不同角落，我們均有安排服務人員負責巡視工作；若遇上任何緊急或突發情況，請即聯絡就近當值的服務人員、或透過船客房內及各樓層的電話撥打服務檯，我們會第一時間為您提供緊急支援及協助。
</p>

<p style="color:#195f9b; font-weight:bold; font-size:16px;">
Q: 當遊輪於行駛時出現停電情況，如何是好？
</p>
<p>
當主電源未能正常運作時，我們將會啟動遊輪上的後備電源，恢復引擎、燈光及升降機等主要儀器的日常運作，維持正常的航行。旅客更可繼續透過船上的溝通設備與外界保持聯絡！
</p>

<p style="color:#195f9b; font-weight:bold; font-size:16px;">
Q: 應穿甚麼服飾上船? 需要正式晚禮服嗎?
</p>
<p>
A: 無論是室內或室外，日間穿著以短褲、T裇、西褲、休閒裙、襯衫及夏日洋裝裝扮，最為合適。如果想徜徉泳池旁，沐浴陽光，則請攜帶２套泳裝（以免另一套弄濕後無法替換），以及一雙休閒鞋（如拖鞋、沙灘鞋或是運動鞋）。另外，從艙房往返泳池之間，您也或許需要準備一些休閒短褲，幾件T裇，或是毛巾外套備用。若您打算到健身中心活動筋骨，可別忘了多帶幾條短褲、T裇及襪子，這樣您就不必汗流浹背地折返艙房換洗。當然還有一雙網球鞋或跑步鞋，也是絕對少不了的。航程或設有船長歡迎酒會及正裝之夜，建議旅客以正式服裝出席(請勿穿著短褲、拖鞋或涼鞋)。
</p>


