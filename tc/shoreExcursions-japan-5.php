<h1>高知•日本</h1>
<h2>K509 高知人文游</h2>
<p>
首先前往【高知城】，它位於高知市中心，由第一代土佐藩主山內一豐始建於1601年，現為國家重要文化遺產, 是日本尚存的三座同時保留有天守閣與追手門城堡之一，更是唯一一座可以把二者拍於一張照片的城堡，拍照留念不容錯過。移步【西洛梅市場】，親身體驗匯集了各具本土特色的百姓店鋪，在美食區品嘗當地的小吃（自理），總會在不經意間有驚喜的發現，最後前往【桂濱海灘】，曾是歷代文人賞月勝地，漫步在沙灘上，感受與歷史的目光重疊。海邊還有可以觀賞海獅表演的水族館（自理)，和與土佐犬小狗親密接觸的土佐犬公園，行程還有到綜合免稅店補貨的機會。行程預留了自理用餐的時間。
</p>
<p>
需時:5小時 
</p>
<p>
費用:$79美元/人
</p>
