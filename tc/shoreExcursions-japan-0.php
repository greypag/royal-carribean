<h2>NG67 長崎養生溫泉遊</h2>
<p>
首先前往【雲仙地獄谷】，因其終年的煙霧裊裊和特殊的地貌而得名，這裡五步一景，十步一觀，水花噴湧，白煙裊裊，雲霧繚繞，到處可見硫磺岩石和地殼曾經活動的印記。最著名的景點有著個有趣的名字：大叫喚地獄，攝氏100多度的熱泉奔騰不息，彷彿千軍萬馬呼嘯而過。徜徉在地獄仙境裡，壯麗而溫馨，浸泡在這宛若隔世般的溫泉中，洗去凡塵雜念，感受難得的恬靜，由來已久的溫泉餐會為您在盡享大自然神奇的同時，帶來久違了的健康的味道。回港前還可以到【Alexander & Sun】免稅店逛逛，這裡備足了各式超值的日本商品和各種促銷，讓您買到盡興。
