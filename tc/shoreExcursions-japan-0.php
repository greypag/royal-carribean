<h1>長崎•日本</h1>
<h2>NG68 長崎經典遊</h2>
<p>
首先將帶您前往【和平公園】，在原子彈降落點修建的這座公園，矗立著一座和平祈念像，高達9.7米，右手指天，緊閉的雙眼說明對原子彈爆炸犧牲者冥福的祈禱。園內的“和平泉”，雕刻了輾轉尋水的少女手記。 1985年中國贈送給長崎的和平少女像，與世界各國贈送的紀念物一起排列，象徵著世界和平。之後遊覽【哥拉巴園】，這裡可以俯瞰整個長崎港的美景。 18世紀的人們對日本的黎明充滿了期待：有滿懷夢想的外國商人，也有壯志激昂的革命志士，還有追求學問的當地青年。哥拉巴園是他們的主要聚集地。如今它以日本最古老的木造洋房而聞名。廣場上還樹立著以哥拉巴夫人為原型的“蝴蝶夫人”銅像。回港前您還可以在【長崎中華街】品嚐各種美食（自費），到【長崎塔購物中心】為發現心儀又超值的手信。
</p>
<p>
需時:6小時 
</p>
<p>
費用:$65美元/人
</p>
