<h2>NH16 芽莊遊船之旅 (Nha Trang River Cruise)</h2>
<p>
首先前往當地小船工廠，了解生產小船的過程。我們將乘坐小船暢遊彩河, 沿途將眺望【鯨魚寺】，這是當地的漁民供奉的鯨魚神以保佑其平安出海。接下來前往【椰子島】，登上椰子島享用當場採摘下來的新鮮椰子汁。之後繼續泛舟前往小村莊看看【古屋】，這裡讓您有機會了解手工製作粘土爐的過程，以及品嚐當地的茶飲及水果。稍作休息後，啟程回到下船碼頭。最後前往【樂天瑪特】選購當地的特產。行程預留了自理午餐的時間。
</p>
<p>
需時:5小時
</p>
<p>
費用:$52美元/人
</p>
<p>&nbsp;</p>
 
<h2>NH19 珍珠島與海上纜車 (Vinpearl Beach with Cable Car & Lunch-VIP)</h2>
<p>
首先乘坐海上纜車，登上3,320公尺高，擁有吉尼斯記錄的跨海纜車將讓您飽覽芽莊市全景。接下來前往位於島上的主題樂園【Vinpeal Land樂園】，佔地面積達20萬平方公尺。這個多功能的主題樂園擁有嘉年華、水上娛樂場地、度假村、高爾夫球場等多種驚險刺激的遊樂設施、水上設施及度假設施。您可以自由游泳，欣賞美麗的海灘,前往度假村享受一次水療，放鬆身心，並於園內享用午餐。
</p>
<p>
需時:7小時
</p>
<p>
費用:$208美元/人
</p>

