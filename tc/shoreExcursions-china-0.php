<h2>XY03 三亞休閒遊 (Sanya on Your Own)</h2>
<p>
從碼頭出發前往【大東海】。大東海位於距三亞市區3公里的兔子尾和鹿回頭兩山之間。月牙形的海灣，遼闊的海面晶瑩如鏡，金沙融融，陽光沙灘，碧水綠樹構成了美麗如春，水暖沙平的大東海。度假區內海濱旅遊設施集中而配套，有嬉水樂園、旅遊潛艇碼頭、潛水和跳水基地等，可常年進行多種水上活動和沙灘運動。（水上活動費用自理）在享受悠閒的海灘時光後返回碼頭。
</p>
<p>
需時:4小時
</p>
<p>
費用:30美元/人
</p>

